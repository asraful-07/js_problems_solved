// logical operator (and, or , not)
// &&, ||, !!

// && operator first value is right and also second value right for work it
if (10 > 0 && 20 > 10) {
  console.log("okay");
}

// || operator just only right than okay print
if (10 > 100 || 20 > 10) {
  console.log("okay");
}

// !! operator true you use to ! and this value false
if (!10 > 0) {
  console.log("okay");
}
